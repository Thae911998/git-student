package student_registration.models;

public class Education {
private int eid;
private String ename;

public Education() {
	super();
}
public int getEid() {
	return eid;
}
public void setEid(int eid) {
	this.eid = eid;
}
public String getEname() {
	return ename;
}
public void setEname(String ename) {
	this.ename = ename;
}
}
